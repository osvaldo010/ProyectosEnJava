    private void formWindowOpened(java.awt.event.WindowEvent evt)//GEN-FIRST:event_formWindowOpened
    {//GEN-HEADEREND:event_formWindowOpened
//        regresar.putClientProperty("JButton.buttonType", "roundRect");
        
        try
        {
            SelectRegistro.datos = (DatosGenerales[]) ManipulacionArchivos.carga(this, "Datos.dat");
            DefaultTableModel modelo = (DefaultTableModel) tblConsultas.getModel();
            for (int i = 0; i < SelectRegistro.datos.length; i++)
            {
                if (SelectRegistro.datos[i] instanceof Personal)
                {
                    String cve = SelectRegistro.datos[i].getCve();
                    String nom = SelectRegistro.datos[i].getNom();
                    String pA = SelectRegistro.datos[i].getPrimerAp();
                    String sA = SelectRegistro.datos[i].getSegundoAp();
                    String sx;
                    if (SelectRegistro.datos[i].getSexo() == 'H')
                    {
                        sx = "Hombre";
                    } else
                    {
                        sx = "Mujer";
                    }
                    String est;
                    if (((Personal) SelectRegistro.datos[i]).getEstatus() == 'B')
                    {
                        est = "Base";
                    } else
                    {
                        est = "Temporal";
                    }
                    String alerg;
                    if (((Personal) SelectRegistro.datos[i]).isAlergias() == true)
                    {
                        alerg = "Si";
                    } else
                    {
                        alerg = "No";
                    }
                    String diab;
                    if (((Personal) SelectRegistro.datos[i]).isDiabetes() == true)
                    {
                        diab = "Si";
                    } else
                    {
                        diab = "No";
                    }
                    String desn;
                    if (((Personal) SelectRegistro.datos[i]).isDesnutricion() == true)
                    {
                        desn = "Si";
                    } else
                    {
                        desn = "No";
                    }
                    String sp;
                    if (((Personal) SelectRegistro.datos[i]).isSobrepeso() == true)
                    {
                        sp = "Si";
                    } else
                    {
                        sp = "No";
                    }
                    String obsd;
                    if (((Personal) SelectRegistro.datos[i]).isObesidad() == true)
                    {
                        obsd = "Si";
                    } else
                    {
                        obsd = "No";
                    }
                    String ots;
                    if (SelectRegistro.datos[i].getOtras().length() == 0)
                    {
                        ots = "No";
                    } else
                    {
                        ots = SelectRegistro.datos[i].getOtras();
                    }

                    String[] fila =
                    {
                        cve, nom, pA, sA, sx, est, alerg, diab, desn, sp, obsd, ots
                    };
                    modelo.addRow(fila);
                }
            }
        } catch (Exception e)
        {
            if (SelectRegistro.datos == null)
            {
                Mensaje.error(this, "No hay registros");
                new SelectOption().setVisible(true);
                dispose();
            } else
            {
                Mensaje.error(this, "Error... " + e);
                new SelectOption().setVisible(true);
                dispose();
            }
        }
    }//GEN-LAST:event_formWindowOpened

