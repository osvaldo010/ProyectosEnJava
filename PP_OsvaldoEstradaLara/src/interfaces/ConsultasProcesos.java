    private void botonAcepActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAcepActionPerformed
    {//GEN-HEADEREND:event_botonAcepActionPerformed
        try
        {
            MenuPrincipal.lista = (LDLC) Archivos.carga(this, "Datos.dat");
            NodoL aux2 = MenuPrincipal.lista.getR();
            aux = null;
            do
            {
                aux2 = aux2.getSiguiente();
                if (((Prioridad) aux2.getObj()).getNoPrioridad() == Integer.valueOf(textoPrio.getText()))
                {
                    aux = aux2;
                }
            } while (aux2 != MenuPrincipal.lista.getR());

            if (aux != null)
            {
                aux3 = ((Prioridad) aux.getObj()).getCola().getAtras().getSiguiente();
                priori.setText(String.valueOf(((Prioridad) aux.getObj()).getNoPrioridad()));
                textoNumPrioridad.setText(String.valueOf(((Prioridad) aux.getObj()).getNoPrioridad()));
                textoNom.setText(((Proceso) aux3.getObj()).getNombre());
                textoQuan.setText(String.valueOf(((Proceso) aux3.getObj()).getQuantums()));
                if (aux3 == ((Prioridad) aux.getObj()).getCola().getAtras())
                {
                    botonSiguiente.setEnabled(false);
                } else
                {
                    botonSiguiente.setEnabled(true);
                }
                botonAnterior.setEnabled(false);
            } else
            {
                Mensajes.error(this, "No se encontró la prioridad.");
                CtrlInterfaz.limpia(textoPrio, textoNom, textoNumPrioridad, textoQuan, priori);
                CtrlInterfaz.cambia(textoPrio);
                botonAnterior.setEnabled(false);
                botonSiguiente.setEnabled(false);
            }
        } catch (Exception e)
        {
            Mensajes.error(this, "Error..." + e.getMessage());
            new MenuPrincipal().setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_botonAcepActionPerformed

