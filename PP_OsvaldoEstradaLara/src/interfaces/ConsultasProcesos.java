    private void botonSiguienteActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonSiguienteActionPerformed
    {//GEN-HEADEREND:event_botonSiguienteActionPerformed
        try
        {
            MenuPrincipal.lista = (LDLC) Archivos.carga(this, "Datos.dat");
            ColasCircularesDinamicas colaAux = new ColasCircularesDinamicas();
            Nodo nodo = aux3.getSiguiente();
            do
            {
                aux3 = ((Prioridad) aux.getObj()).getCola().getAtras().getSiguiente();
                if (nodo == aux3.getSiguiente())
                {
                    textoNumPrioridad.setText(String.valueOf(((Proceso) nodo.getObj()).getNoPrioridad()));
                    textoNom.setText(((Proceso) nodo.getObj()).getNombre());
                    textoQuan.setText(String.valueOf(((Proceso) nodo.getObj()).getQuantums()));
                }
                colaAux.inserta(((Prioridad) aux.getObj()).getCola().elimina());
            } while (((Prioridad) aux.getObj()).getCola().getAtras() != null);
            ((Prioridad) aux.getObj()).setCola(colaAux);

            aux3 = nodo;

            if (aux3 == ((Prioridad) aux.getObj()).getCola().getAtras())
            {
                botonSiguiente.setEnabled(false);
            }
            botonAnterior.setEnabled(true);
        } catch (Exception e)
        {
            Mensajes.error(this, "Error..." + e.getMessage());
        }
    }//GEN-LAST:event_botonSiguienteActionPerformed

