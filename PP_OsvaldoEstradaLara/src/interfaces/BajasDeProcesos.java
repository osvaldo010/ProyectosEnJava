    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonAceptarActionPerformed
    {//GEN-HEADEREND:event_botonAceptarActionPerformed
        try
        {
        if (MenuPrincipal.lista.getR() != null)
        {
            boolean b = Manipulacion.verificaNombreProceso(MenuPrincipal.lista, textoNombre.getText());

            if (b != false)
            {
                NodoL aux = MenuPrincipal.lista.getR();
                Nodo aux3;
                do
                {
                    aux = aux.getSiguiente();
                    ColasCircularesDinamicas cola = new ColasCircularesDinamicas();
                    do
                    {
                        aux3 = ((Prioridad) aux.getObj()).getCola().elimina();
                        if (aux3.getEtiqueta().equals(textoNombre.getText()))
                        {
                            textoNumPrioridad.setText(String.valueOf(((Prioridad) aux.getObj()).getNoPrioridad()));
                            textoNom.setText(((Proceso) aux3.getObj()).getNombre());
                            textoQuan.setText(String.valueOf(((Proceso) aux3.getObj()).getQuantums()));
                        }
                        cola.inserta(aux3);
                    } while (((Prioridad) aux.getObj()).getCola().getAtras() != null);
                    ((Prioridad) aux.getObj()).setCola(cola);
                } while (aux != MenuPrincipal.lista.getR());
            } else
            {
                Mensajes.error(this, "No se encontró el proceso.");
            }
            CtrlInterfaz.limpia(textoNombre);
        } else
        {
            Mensajes.error(this, "Error. Lista vacía.");
            new MenuPrincipal().setVisible(true);
            dispose();
        }
        } catch (Exception e)
        {
            Mensajes.error(this, "Error..." + e.getMessage());
        }

    }//GEN-LAST:event_botonAceptarActionPerformed

