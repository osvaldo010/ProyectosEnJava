    private void botonBorrarProcesoActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonBorrarProcesoActionPerformed
    {//GEN-HEADEREND:event_botonBorrarProcesoActionPerformed
        try
        {
            MenuPrincipal.lista = (LDLC) Archivos.carga(this, "Datos.dat");

            if (textoNombre.getText() == null)
            {
                Mensajes.error(this, "Error, no ha ingresado un proceso para borrar.");
                CtrlInterfaz.limpia(textoNombre);
                CtrlInterfaz.cambia(textoNombre);
            } else
            {
                MenuPrincipal.lista = Manipulacion.bajaProceso(MenuPrincipal.lista, textoNom.getText());
                Mensajes.exito(this, "Se borró el proceso con éxito");
                Archivos.guarda(this, MenuPrincipal.lista, "Datos.dat");
                CtrlInterfaz.limpia(textoNom, textoNombre, textoNumPrioridad, textoQuan);
                CtrlInterfaz.cambia(textoNombre);
                if (MenuPrincipal.lista.getR() == null)
                {
                    Mensajes.error(this, "Lista vacía.");
                    new MenuPrincipal().setVisible(true);
                    dispose();
                }
                
            }
        } catch (Exception e)
        {
            Mensajes.error(this, "Error..." + e.getMessage());
        }
    }//GEN-LAST:event_botonBorrarProcesoActionPerformed

